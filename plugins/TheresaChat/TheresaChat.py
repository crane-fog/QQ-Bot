    async def load_context_from_db(self, group_id: int) -> list:
        async_sessions = sessionmaker(
            bind=self.bot.database, class_=AsyncSession, expire_on_commit=False
        )
        context = []
        async with async_sessions() as session:
            stmt = (
                select(Message)
                .where(Message.group_id == group_id)
                .order_by(desc(Message.send_time))
                .limit(self.context_length)
            )
            result = await session.execute(stmt)
            rows = result.scalars().all()

        for row in reversed(rows):
            if row.user_id == 0:
                context.append(
                    {
                        "role": "assistant",
                        "content": row.msg,
                    }
                )
            else:
                context.append(
                    {
                        "role": "user",
                        "content": f"{row.user_nickname}(ç¾¤åç‰‡ï¼š{row.user_card}ï¼Œidï¼š{row.user_id})è¯´ï¼š{row.msg}",
                    }
                )
        return context

